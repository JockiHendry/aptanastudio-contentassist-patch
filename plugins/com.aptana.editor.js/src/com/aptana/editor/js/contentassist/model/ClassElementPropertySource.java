package com.aptana.editor.js.contentassist.model;

import java.util.EnumSet;
import java.util.Set;

import com.aptana.js.core.model.ClassElement;

public class ClassElementPropertySource extends TypeElementPropertySource
{

	public ClassElementPropertySource(ClassElement adaptableObject)
	{
		super(adaptableObject);
	}

	@Override
	protected Set<Property> getPropertyInfoSet()
	{
		return EnumSet.allOf(Property.class);
	}
}
