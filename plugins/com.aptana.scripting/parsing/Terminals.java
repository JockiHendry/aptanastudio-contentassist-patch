package com.aptana.scope.parsing;

/**
 * This class lists terminals used by the
 * grammar specified in "Scope.grammar".
 */
public class Terminals {
	static public final short EOF = 0;
	static public final short LPAREN = 1;
	static public final short IDENTIFIER = 2;
	static public final short PIPE = 3;
	static public final short COMMA = 4;
	static public final short MINUS = 5;
	static public final short AMPERSAND = 6;
	static public final short RPAREN = 7;
}
